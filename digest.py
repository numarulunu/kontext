def process_digests(auto: bool = False, dry_run: bool = False,
                    specific_file: Path = None, min_grade: int = 7) -> dict:
    """Process all pending digests or a specific file.

    Returns {files_processed, candidates_found, candidates_fresh, imported}.
    """
    db = KontextDB()
    results = {
        "files_processed": 0,
        "candidates_found": 0,
        "candidates_fresh": 0,
        "imported": 0,
    }

    if specific_file:
        files = [specific_file]
    else:
        if not MANIFEST.exists():
            _log.warning("No manifest found — run update-memory.sh first")
            print("No digest manifest found. Run the backup system first.")
            db.close()
            return results
        # Parse manifest to get file list
        files = list(DIGEST_DIR.glob("*.md"))
        files = [f for f in files if f.name != "_manifest.md"]

    all_candidates = []
    successfully_processed: list[Path] = []  # for move-to-_processed below
    for filepath in files:
        if not filepath.exists():
            _log.warning(f"Digest file missing: {filepath}")
            continue

        candidates = extract_candidates(filepath)
        results["files_processed"] += 1
        results["candidates_found"] += len(candidates)
        all_candidates.extend(candidates)
        successfully_processed.append(filepath)
        _log.info(f"EXTRACT: {filepath.name} → {len(candidates)} candidates")

    # Deduplicate across digest files (same message can appear in overlapping digests).
    # Two-pass: cheap 80-char prefix exact-match, then fuzzy 0.85 similarity over
    # what survives — catches "Active students: 24" vs "Active students: 24 paying"
    # the same way sync.py already does for flat-file imports.
    from difflib import SequenceMatcher
    seen_prefixes = set()
    unique_candidates = []
    for c in all_candidates:
        key = c["text"][:80]
        if key in seen_prefixes:
            continue
        # Fuzzy check against already-kept candidates
        is_dup = False
        c_lower = c["text"].lower()
        for kept in unique_candidates:
            if SequenceMatcher(None, c_lower, kept["text"].lower()).ratio() >= 0.85:
                is_dup = True
                break
        if is_dup:
            continue
        seen_prefixes.add(key)
        unique_candidates.append(c)

    # Deduplicate against existing database
    fresh = deduplicate_candidates(unique_candidates, db)
    results["candidates_fresh"] = len(fresh)

    if dry_run:
        print(f"Would process {len(fresh)} fresh candidates from {results['files_processed']} files")
        for c in fresh[:20]:
            print(f"  [{c['type']}, g{c['grade']}] {c['text'][:80]}")
        db.close()
        return results

    if auto:
        import_results = auto_import(fresh, db, min_grade=min_grade)
        results["imported"] = import_results["imported"]
        _log.info(f"AUTO-IMPORT: {import_results}")

        # Re-export affected files
        if import_results["imported"] > 0:
            from export import export_all, export_memory_index
            from mcp_server import find_memory_dir
            mem_dir = find_memory_dir()
            if mem_dir:
                export_all(db, mem_dir)
                export_memory_index(db, mem_dir)
    else:
        # Write candidates file for manual review
        write_candidates_file(fresh, CANDIDATES_FILE)
        print(f"Wrote {len(fresh)} candidates to {CANDIDATES_FILE}")

    # Move successfully processed files to _processed/ so the next run doesn't
    # re-parse them. DB-level dedup makes re-parsing safe but wasteful, and
    # orphans in _digests/ now clearly mean "unfinished work" — useful signal
    # for partial-crash recovery.
    if not specific_file and successfully_processed:
        processed_dir = DIGEST_DIR / "_processed"
        try:
            processed_dir.mkdir(exist_ok=True)
            for fp in successfully_processed:
                target = processed_dir / fp.name
                if target.exists():
                    target.unlink()  # idempotent re-runs
                fp.rename(target)
            _log.info(f"ARCHIVED: {len(successfully_processed)} files → {processed_dir}")
        except OSError as e:
            _log.warning(f"ARCHIVE: failed to move processed files — {e}")

    # Clear the pending flag
    if PENDING_FLAG.exists() and not specific_file:
        PENDING_FLAG.unlink()
        _log.info("Cleared _digest-pending flag")

    db.close()
    return results


def main() -> int:
    parser = argparse.ArgumentParser(description="Kontext Digest Processor")
    parser.add_argument("--auto", action="store_true", help="Auto-import high-confidence facts (grade >= 7)")
    parser.add_argument("--dry-run", action="store_true", help="Show what would be extracted")
    parser.add_argument("--file", type=str, help="Process a specific digest file")
    parser.add_argument("--min-grade", type=int, default=7, help="Minimum grade for auto-import (default 7)")
    args = parser.parse_args()

    specific = Path(args.file) if args.file else None
    results = process_digests(
        auto=args.auto,
        dry_run=args.dry_run,
        specific_file=specific,
        min_grade=args.min_grade,
    )

    print(f"\nDigest processing complete:")
    print(f"  Files processed:  {results['files_processed']}")
    print(f"  Candidates found: {results['candidates_found']}")
    print(f"  Fresh (new):      {results['candidates_fresh']}")
    print(f"  Imported:         {results['imported']}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
