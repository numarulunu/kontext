# digest.py
"""
Kontext Digest Processor — extract memory-worthy facts from conversation digests.

Reads digest files generated by the Backup System, extracts user messages
and key exchanges, produces a list of candidate facts for review or
auto-import into the Kontext database.

The processor works in two modes:
  1. Extract mode (default) — pulls candidate facts from digests, writes
     them to _digest_candidates.md for review
  2. Auto mode — imports high-confidence facts directly into the database

Usage:
    python digest.py                    # Extract candidates from pending digests
    python digest.py --auto             # Auto-import high-confidence facts
    python digest.py --dry-run          # Show what would be extracted
    python digest.py --file <path>      # Process a specific digest file
"""

