# ---------------------------------------------------------------------------
# Haiku distillation — turns raw user messages into clean third-person facts
# ---------------------------------------------------------------------------

HAIKU_MODEL = "claude-haiku-4-5-20251001"

_DISTILL_PROMPT = (
    "Rewrite the raw chat message below as a single-line third-person memory fact "
    "for a personal knowledge base owned by a user named Ionut.\n\n"
    "Rules:\n"
    "- Output ONE line under 120 characters.\n"
    "- Third person ('Ionut...', 'User...'), never first person.\n"
    "- Drop hedges, filler, conversational framing. Keep numbers, names, dates, decisions.\n"
    "- If the message contains no durable fact (just chatter), output exactly: SKIP\n"
    "- No quotes, no markdown, no preamble. Just the fact, or SKIP.\n\n"
    "Message:\n{text}"
)

_distill_unavailable = False  # set after first failure to avoid retry storms


def distill_with_haiku(text: str) -> str | None:
    """Rewrite a raw user message as a clean third-person fact via Claude Haiku.

    Shells out to the `claude` CLI (one-shot mode) so we reuse Claude Code's
    existing auth — no API key plumbing required. Returns the distilled fact,
    or None if:
      - The `claude` CLI is missing or fails to launch
      - Haiku judges the message has no durable content (returns 'SKIP')
      - The subprocess errors / times out

    Caller MUST handle None — do not fall back to the raw message, since the
    whole point of distillation is to keep raw chatter out of memory.
    """
    global _distill_unavailable
    if _distill_unavailable:
        return None
    import subprocess, shutil, os, tempfile, json as _json
    from pathlib import Path as _Path
    cli = shutil.which("claude")
    if not cli:
        _log.warning("DISTILL: `claude` CLI not on PATH — distillation disabled for this run")
        _distill_unavailable = True
        return None
    # Batch-mode hardening. The env var alone is insufficient: even if the
    # hooks skip, `claude --print` still cold-boots every MCP server in the
    # user-scope settings.json (Serena LSP init alone is 10-30s). With
    # parallel workers that's a certain deadlock. The fix is an empty
    # --mcp-config file plus --strict-mcp-config, which tells Claude Code
    # to load ZERO MCP servers for this subprocess.
    empty_mcp = _Path(tempfile.gettempdir()) / "kontext-empty-mcp.json"
    if not empty_mcp.exists():
        empty_mcp.write_text(_json.dumps({"mcpServers": {}}), encoding="utf-8")
    env = os.environ.copy()
    env["KONTEXT_SKIP_HOOKS"] = "1"  # belt + suspenders with --strict-mcp-config
    scratch_cwd = tempfile.gettempdir()
    try:
        proc = subprocess.run(
            [
                cli, "-p", _DISTILL_PROMPT.format(text=text),
                "--model", HAIKU_MODEL,
                "--strict-mcp-config",
                "--mcp-config", str(empty_mcp),
            ],
            capture_output=True, text=True, timeout=45,
            encoding="utf-8", errors="replace",
            env=env, cwd=scratch_cwd,
        )
    except (subprocess.TimeoutExpired, OSError) as e:
        _log.warning(f"DISTILL: subprocess failed — {e}")
        return None
    if proc.returncode != 0:
        _log.warning(f"DISTILL: claude CLI rc={proc.returncode} stderr={proc.stderr[:200]}")
        return None
    out = (proc.stdout or "").strip()
    if not out or out.upper() == "SKIP":
        return None
    # Safety: enforce single line + length cap even if model misbehaves
    out = out.splitlines()[0].strip().strip('"').strip("'")
    if out.upper() == "SKIP" or not out:
        return None
    if len(out) > 200:
        out = out[:197] + "..."
    return out


# ---------------------------------------------------------------------------
# Patterns that signal memory-worthy content in user messages
# ---------------------------------------------------------------------------

_HIGH_SIGNAL_PATTERNS = [
    # Decisions (EN + RO)
    (re.compile(r"\b(I('m| am) going (with|to)|decided|choosing|switching to|killing|dropping|cancell?ing|am decis|merg pe|m-am hotărât|renunț la|vreau să)\b", re.I), "decision", 8),
    # Facts about self (EN + RO)
    (re.compile(r"\b(I have|I got|I('m| am)|my .+ is|I started|I stopped|I moved|I bought|I sold|eu sunt|am început|m-am mutat|am cumpărat|am vândut)\b", re.I), "self_fact", 6),
    # Corrections (EN + RO)
    (re.compile(r"\b(actually|no,? it'?s|correction|not \d+,? (it'?s|but) \d+|wrong|update that|de fapt|nu e așa|am greșit|corecție)\b", re.I), "correction", 9),
    # Status changes (EN + RO)
    (re.compile(r"\b(is live|launched|shipped|deployed|stalled|paused|cancelled|finished|completed|am lansat|am livrat|s-a blocat|am terminat|am anulat|e live)\b", re.I), "status_change", 8),
    # Financial (EN + RO)
    (re.compile(r"\b(\$\d+|€\d+|\d+ ?(RON|EUR|USD|lei)|revenue|income|tax|invoice|PFA|ANAF|venit|factură|impozit|plată|taxe)\b", re.I), "financial", 7),
    # Preferences / feedback (EN + RO)
    (re.compile(r"\b(don'?t|stop|never|always|I (like|prefer|want|hate|need)|from now on|prefer|urăsc|îmi place|nu vreau|am nevoie|de acum)\b", re.I), "preference", 7),
    # People (EN + RO)
    (re.compile(r"\b(Luiza|mama|mother|student|client|friend|elev|elevă|prieten|prietenă)\b", re.I), "people", 6),
    # Goals / plans (EN + RO)
    (re.compile(r"\b(goal|plan|target|deadline|by (January|February|March|April|May|June|July|August|September|October|November|December)|obiectiv|termen|până în (ianuarie|februarie|martie|aprilie|mai|iunie|iulie|august|septembrie|octombrie|noiembrie|decembrie))\b", re.I), "goal", 7),
    # Numbers that matter (EN + RO)
    (re.compile(r"\b\d{2,}\s*(students?|reviews?|subscribers?|followers?|clients?|lessons?|sessions?|elevi|recenzii|abonați|clienți|lecții|ședințe)\b", re.I), "metric", 8),
]

# Patterns that indicate noise (skip these messages)
_NOISE_PATTERNS = [
    re.compile(r"^\s*(ok|yes|no|sure|thanks|thank you|got it|cool|nice|perfect|good|great|fine|hmm|hm)\s*[.!]?\s*$", re.I),
    re.compile(r"^\s*(hello|hi|hey|yo)\s*[.!]?\s*$", re.I),
    # Romanian noise
    re.compile(r"^\s*(da|nu|bine|ok|mersi|mulțumesc|super|perfect|gata|sigur)\s*[.!]?\s*$", re.I),
    re.compile(r"^\s*(salut|bună|hei|neața)\s*[.!]?\s*$", re.I),
    re.compile(r"^\[Tool:", re.I),  # Tool use markers in digests
    re.compile(r"^#+\s*\[", re.I),  # Timestamp headers
    re.compile(r"Base directory for this skill:", re.I),  # Skill system messages
    re.compile(r"<system-reminder>", re.I),  # System reminders
    re.compile(r"^```", re.I),  # Code blocks
    re.compile(r"^\s*\{", re.I),  # JSON fragments
]


# ---------------------------------------------------------------------------
# Digest parsing
# ---------------------------------------------------------------------------

def parse_digest(filepath: Path) -> list[dict]:
    """Parse a digest markdown file into a list of messages.

    Returns list of {role, text, timestamp, session} dicts.
    Only keeps USER messages (Claude responses are context, not memory).
    """
    content = filepath.read_text(encoding="utf-8", errors="replace")
    messages = []
    current_session = 0
    current_role = None
    current_text_lines = []
    current_timestamp = ""

    session_re = re.compile(r"^## Session (\d+)")
    msg_re = re.compile(r"^### \[(\d{2}:\d{2})\] \*\*(\w+)\*\*")

    for line in content.split("\n"):
        session_match = session_re.match(line)
        if session_match:
            # Flush previous message
            if current_role == "USER" and current_text_lines:
                text = "\n".join(current_text_lines).strip()
                if text and len(text) > 10:
                    messages.append({
                        "role": "USER",
                        "text": text,
                        "timestamp": current_timestamp,
                        "session": current_session,
                    })
            current_session = int(session_match.group(1))
            current_role = None
            current_text_lines = []
            continue

        msg_match = msg_re.match(line)
        if msg_match:
            # Flush previous message
            if current_role == "USER" and current_text_lines:
                text = "\n".join(current_text_lines).strip()
                if text and len(text) > 10:
                    messages.append({
                        "role": "USER",
                        "text": text,
                        "timestamp": current_timestamp,
                        "session": current_session,
                    })
            current_timestamp = msg_match.group(1)
            current_role = msg_match.group(2)
            current_text_lines = []
            continue

        if current_role == "USER":
            current_text_lines.append(line)

    # Flush last message
    if current_role == "USER" and current_text_lines:
        text = "\n".join(current_text_lines).strip()
        if text and len(text) > 10:
            messages.append({
                "role": "USER",
                "text": text,
                "timestamp": current_timestamp,
                "session": current_session,
            })

    return messages


def is_noise(text: str) -> bool:
    """Check if a message is noise (greetings, acknowledgments, tool markers)."""
    for pattern in _NOISE_PATTERNS:
        if pattern.match(text):
            return True
    return False


def score_message(text: str) -> list[dict]:
    """Score a user message for memory-worthiness.

    Returns list of {pattern_type, grade, match} for all matching patterns.
    """
    hits = []
    for pattern, ptype, grade in _HIGH_SIGNAL_PATTERNS:
        match = pattern.search(text)
        if match:
            hits.append({
                "type": ptype,
                "grade": grade,
                "match": match.group(0),
            })
    return hits


# ---------------------------------------------------------------------------
# Candidate extraction
# ---------------------------------------------------------------------------

def extract_candidates(filepath: Path) -> list[dict]:
    """Extract memory-worthy candidates from a single digest file.

    Returns list of {text, grade, type, source, session, timestamp} dicts.
    """
    messages = parse_digest(filepath)
    candidates = []

    project_name = filepath.stem.replace("-", " ").title()
    today = datetime.now(timezone.utc).strftime("%Y-%m")

    for msg in messages:
        text = msg["text"]
        if is_noise(text):
            continue

        # Skip very short messages (low information density)
        if len(text) < 30:
            continue

        # Skip very long messages (likely code or system dumps, not facts)
        if len(text) > 1000:
            continue

        hits = score_message(text)
        if not hits:
            continue

        # Require either a high-grade hit (8+) or multiple pattern matches
        best = max(hits, key=lambda h: h["grade"])
        if best["grade"] < 8 and len(hits) < 2:
            continue

        # Truncate for storage
        if len(text) > 300:
            text = text[:297] + "..."

        candidates.append({
            "text": text,
            "grade": best["grade"],
            "type": best["type"],
            "source": f"[digest {today}]",
            "session": msg["session"],
            "timestamp": msg["timestamp"],
            "project": project_name,
        })

    return candidates


def deduplicate_candidates(candidates: list[dict], db: KontextDB) -> list[dict]:
    """Remove candidates that already exist in the database (85% similarity threshold)."""
    from difflib import SequenceMatcher

    # Load all DB facts once for efficient comparison
    all_entries = db.conn.execute("SELECT fact FROM entries").fetchall()
    db_facts = [row[0].lower() for row in all_entries]

    fresh = []
    for c in candidates:
        text_lower = c["text"].lower()
        is_dup = any(
            SequenceMatcher(None, text_lower, f).ratio() >= 0.85
            for f in db_facts
        )
        if not is_dup:
            fresh.append(c)
    return fresh


