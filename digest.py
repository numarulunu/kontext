def write_candidates_file(candidates: list[dict], output_path: Path):
    """Write candidates to a markdown file for LLM distillation and review.

    Format is designed for the /kontext process-digest subagents to read,
    distill raw messages into atomic facts, and import via kontext_write.
    """
    lines = [
        "# Digest Candidates — Awaiting Distillation",
        f"**Generated:** {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M')} UTC",
        f"**Candidates:** {len(candidates)}",
        "",
        "These are raw user messages extracted by pattern matching.",
        "They need LLM distillation before import — convert each into atomic facts.",
        "",
        "**Distillation rules:**",
        "- Convert raw message → 1-3 atomic facts (single statement per fact)",
        "- Strip conversational filler, keep only the fact itself",
        '- Example: "I have 42 active students right now, up from 38" →',
        "  - `Active students: 42 (up from 38)` [grade 9, self_fact]",
        "- Include dated source tag from the digest session date",
        "- Grade 8-10 for decisions/identity/metrics, 5-7 for soft context",
        "",
        "Run `/kontext process-digest` to trigger LLM distillation and import.",
        "",
    ]

    by_project = {}
    for c in candidates:
        by_project.setdefault(c["project"], []).append(c)

    for project, items in sorted(by_project.items()):
        lines.append(f"## {project}")
        lines.append("")
        for c in items:
            lines.append(f"- **[{c['type']}, g{c['grade']}]** {c['text']}")
            lines.append(f"  → target: `{route_to_file(c['project'], c['type'])}` | source: `{c.get('source', 'unknown')}`")
        lines.append("")

    output_path.write_text("\n".join(lines), encoding="utf-8")


def auto_import(candidates: list[dict], db: KontextDB, min_grade: int = 7,
                distill: bool = True) -> dict:
    """Auto-import high-confidence candidates into the database.

    Each candidate above min_grade is sent through Haiku distillation
    (`distill_with_haiku`) to convert raw first-person chat into a clean
    third-person memory fact before storage. Candidates that Haiku marks SKIP
    or that fail distillation are dropped — we never store raw chatter.

    Set distill=False to skip the LLM pass (used by tests).

    Returns {imported, skipped, distill_dropped, conflicts_found, total}.
    """
    imported = 0
    skipped = 0
    distill_dropped = 0

    for c in candidates:
        if c["grade"] < min_grade:
            skipped += 1
            continue

        if distill:
            clean = distill_with_haiku(c["text"])
            if clean is None:
                distill_dropped += 1
                _log.info(f"DISTILL_SKIP: g{c['grade']} {c['text'][:60]}")
                continue
            fact_text = clean
        else:
            fact_text = c["text"]

        target_file = route_to_file(c["project"], c["type"])
        db.add_entry(
            file=target_file,
            fact=fact_text,
            source=c["source"],
            grade=c["grade"],
            tier="active",
        )
        imported += 1
        _log.info(f"IMPORT: [{target_file}] g{c['grade']} {fact_text[:60]}")

    # Run conflict detection after import to catch contradictions
    conflicts_found = 0
    if imported > 0:
        new_conflicts = db.detect_conflicts()
        conflicts_found = len(new_conflicts)
        if conflicts_found > 0:
            _log.warning(f"IMPORT: {conflicts_found} new conflict(s) detected after import")

    return {"imported": imported, "skipped": skipped, "distill_dropped": distill_dropped,
            "conflicts_found": conflicts_found, "total": len(candidates)}


