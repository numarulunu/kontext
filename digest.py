def write_candidates_file(candidates: list[dict], output_path: Path):
    """Write candidates to a markdown file for LLM distillation and review.

    Format is designed for the /kontext process-digest subagents to read,
    distill raw messages into atomic facts, and import via kontext_write.
    """
    lines = [
        "# Digest Candidates — Awaiting Distillation",
        f"**Generated:** {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M')} UTC",
        f"**Candidates:** {len(candidates)}",
        "",
        "These are raw user messages extracted by pattern matching.",
        "They need LLM distillation before import — convert each into atomic facts.",
        "",
        "**Distillation rules:**",
        "- Convert raw message → 1-3 atomic facts (single statement per fact)",
        "- Strip conversational filler, keep only the fact itself",
        '- Example: "I have 42 active students right now, up from 38" →',
        "  - `Active students: 42 (up from 38)` [grade 9, self_fact]",
        "- Include dated source tag from the digest session date",
        "- Grade 8-10 for decisions/identity/metrics, 5-7 for soft context",
        "",
        "Run `/kontext process-digest` to trigger LLM distillation and import.",
        "",
    ]

    by_project = {}
    for c in candidates:
        by_project.setdefault(c["project"], []).append(c)

    for project, items in sorted(by_project.items()):
        lines.append(f"## {project}")
        lines.append("")
        for c in items:
            lines.append(f"- **[{c['type']}, g{c['grade']}]** {c['text']}")
            lines.append(f"  → target: `{route_to_file(c['project'], c['type'])}` | source: `{c.get('source', 'unknown')}`")
        lines.append("")

    output_path.write_text("\n".join(lines), encoding="utf-8")


def auto_import(candidates: list[dict], db: KontextDB, min_grade: int = 7) -> dict:
    """Auto-import high-confidence candidates into the database.

    Only imports candidates with grade >= min_grade.
    Runs conflict detection after import to catch contradictions.
    Returns {imported, skipped, conflicts_found, total}.
    """
    imported = 0
    skipped = 0

    for c in candidates:
        if c["grade"] < min_grade:
            skipped += 1
            continue

        target_file = route_to_file(c["project"], c["type"])
        db.add_entry(
            file=target_file,
            fact=c["text"],
            source=c["source"],
            grade=c["grade"],
            tier="active",
        )
        imported += 1
        _log.info(f"IMPORT: [{target_file}] g{c['grade']} {c['text'][:60]}")

    # Run conflict detection after import to catch contradictions
    conflicts_found = 0
    if imported > 0:
        new_conflicts = db.detect_conflicts()
        conflicts_found = len(new_conflicts)
        if conflicts_found > 0:
            _log.warning(f"IMPORT: {conflicts_found} new conflict(s) detected after import")

    return {"imported": imported, "skipped": skipped, "conflicts_found": conflicts_found, "total": len(candidates)}


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def process_digests(auto: bool = False, dry_run: bool = False,
                    specific_file: Path = None, min_grade: int = 7) -> dict:
    """Process all pending digests or a specific file.

    Returns {files_processed, candidates_found, candidates_fresh, imported}.
    """
    db = KontextDB()
    results = {
        "files_processed": 0,
        "candidates_found": 0,
        "candidates_fresh": 0,
        "imported": 0,
    }

    if specific_file:
        files = [specific_file]
    else:
        if not MANIFEST.exists():
            _log.warning("No manifest found — run update-memory.sh first")
            print("No digest manifest found. Run the backup system first.")
            db.close()
            return results
        # Parse manifest to get file list
        files = list(DIGEST_DIR.glob("*.md"))
        files = [f for f in files if f.name != "_manifest.md"]

    all_candidates = []
    for filepath in files:
        if not filepath.exists():
            _log.warning(f"Digest file missing: {filepath}")
            continue

        candidates = extract_candidates(filepath)
        results["files_processed"] += 1
        results["candidates_found"] += len(candidates)
        all_candidates.extend(candidates)
        _log.info(f"EXTRACT: {filepath.name} → {len(candidates)} candidates")

    # Deduplicate across digest files (same message can appear in overlapping digests)
    seen_texts = set()
    unique_candidates = []
    for c in all_candidates:
        key = c["text"][:80]
        if key not in seen_texts:
            seen_texts.add(key)
            unique_candidates.append(c)

    # Deduplicate against existing database
    fresh = deduplicate_candidates(unique_candidates, db)
    results["candidates_fresh"] = len(fresh)

    if dry_run:
        print(f"Would process {len(fresh)} fresh candidates from {results['files_processed']} files")
        for c in fresh[:20]:
            print(f"  [{c['type']}, g{c['grade']}] {c['text'][:80]}")
        db.close()
        return results

    if auto:
        import_results = auto_import(fresh, db, min_grade=min_grade)
        results["imported"] = import_results["imported"]
        _log.info(f"AUTO-IMPORT: {import_results}")

        # Re-export affected files
        if import_results["imported"] > 0:
            from export import export_all, export_memory_index
            from mcp_server import find_memory_dir
            mem_dir = find_memory_dir()
            if mem_dir:
                export_all(db, mem_dir)
                export_memory_index(db, mem_dir)
    else:
        # Write candidates file for manual review
        write_candidates_file(fresh, CANDIDATES_FILE)
        print(f"Wrote {len(fresh)} candidates to {CANDIDATES_FILE}")

    # Clear the pending flag
    if PENDING_FLAG.exists() and not specific_file:
        PENDING_FLAG.unlink()
        _log.info("Cleared _digest-pending flag")

    db.close()
    return results


def main() -> int:
    parser = argparse.ArgumentParser(description="Kontext Digest Processor")
    parser.add_argument("--auto", action="store_true", help="Auto-import high-confidence facts (grade >= 7)")
    parser.add_argument("--dry-run", action="store_true", help="Show what would be extracted")
    parser.add_argument("--file", type=str, help="Process a specific digest file")
    parser.add_argument("--min-grade", type=int, default=7, help="Minimum grade for auto-import (default 7)")
    args = parser.parse_args()

    specific = Path(args.file) if args.file else None
    results = process_digests(
        auto=args.auto,
        dry_run=args.dry_run,
        specific_file=specific,
        min_grade=args.min_grade,
    )

    print(f"\nDigest processing complete:")
    print(f"  Files processed:  {results['files_processed']}")
    print(f"  Candidates found: {results['candidates_found']}")
    print(f"  Fresh (new):      {results['candidates_fresh']}")
    print(f"  Imported:         {results['imported']}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
