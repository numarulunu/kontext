_PROJECT_TO_FILE = {
    "finance": "user_financial_architecture.md",
    "pfa": "user_financial_architecture.md",
    "contabilitate": "user_financial_architecture.md",
    "skool": "project_vocality_content.md",
    "youtube": "project_vocality_content.md",
    "personal context": "user_identity.md",
    "kontext": "design_principles.md",
    "tool auditor": "tool_registry.md",
}


def route_to_file(project_name: str, candidate_type: str) -> str:
    """Determine which memory file a candidate belongs to."""
    name_lower = project_name.lower()
    for keyword, filename in _PROJECT_TO_FILE.items():
        if keyword in name_lower:
            return filename

    # Route by type if project doesn't match
    type_routes = {
        "financial": "user_financial_architecture.md",
        "preference": "feedback_ai_interaction.md",
        "correction": "feedback_ai_interaction.md",
        "people": "user_identity.md",
        "self_fact": "user_identity.md",
        "goal": "project_goals.md",
        "decision": "project_goals.md",
        "metric": "project_goals.md",
        "status_change": "project_goals.md",
    }
    return type_routes.get(candidate_type, "user_identity.md")


